window.onClickUpArrow = (e) => {
  let spanNode = e.target.parentNode;
  if (spanNode.tagName === 'g') {
    spanNode = spanNode.parentNode.parentNode;
  }

  const parentNode = spanNode.parentNode;
  parentNode.removeChild(spanNode);
  const UpEl = document.createElement("span");
  UpEl.className = 'keyword-item-show-more';
  UpEl.addEventListener('click', onClickDownArrow);
  UpEl.innerHTML = getDownArrowSVG();

  parentNode.appendChild(UpEl);
  parentNode.parentNode.querySelector('.keyword-item-content').className = 'keyword-item-content-hidden';
};

